class Solution {
public:
    vector<int> findMaxRow(vector<vector<int>> mat, int N) 
    {
        vector<int> ans = {0,0};
        int j;
        for(int i=0;i<N;i++)
        {
            j = N-1-ans[1];
            while(mat[i][j] == 1 and j>=0)
            {
                ans[0] = i;
                j--;
            }
            ans[1] = N-1-j;
        }
        return ans;
    }
};

